// for loop problem  (Initialization; condition; updatation)

// print numbers from 0 to n using for loop
#include<iostream>
using namespace std;

int main()
{
    int n;
    cout << "Enter last Number : ";
    cin >> n ;

     for (int i = 0; i <= n; i++)
     {
        cout << i << " "; 
     }
     cout << endl;
     return 0;
}
